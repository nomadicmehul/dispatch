export type AuthMethod = "api-key" | "claude-code";

// ---------------------------------------------------------------------------
// Workflow: api-key  (personal accounts — provide your own ANTHROPIC_API_KEY)
// ---------------------------------------------------------------------------
const API_KEY_WORKFLOW = (cronSchedule: string, maxIssues: number, draft: boolean, labels: string[]) => `# Dispatch — Nightly Issue Solver
# Automatically solves open GitHub issues and creates pull requests.
# Generated by: dispatch schedule
# Auth: api-key (personal account — uses ANTHROPIC_API_KEY secret)

name: Dispatch Nightly Run

on:
  schedule:
    # Runs at ${cronSchedule} UTC — adjust to your preferred time
    - cron: "${cronSchedule}"
  workflow_dispatch:
    # Allow manual trigger from GitHub Actions UI
    inputs:
      dry_run:
        description: "Dry run (preview only, no PRs)"
        required: false
        default: "false"
        type: boolean
      max_issues:
        description: "Max issues to process"
        required: false
        default: "${maxIssues}"
        type: string

permissions:
  contents: write
  pull-requests: write
  issues: write

jobs:
  dispatch:
    runs-on: ubuntu-latest
    timeout-minutes: 120

    steps:
      - name: Checkout repository
        uses: actions/checkout@v4
        with:
          fetch-depth: 0

      - name: Setup Node.js
        uses: actions/setup-node@v4
        with:
          node-version: "20"

      - name: Install Claude Code
        run: npm install -g @anthropic-ai/claude-code

      - name: Install Dispatch
        run: npm install -g dispatch-ai@beta

      - name: Run Dispatch
        env:
          GITHUB_TOKEN: \${{ secrets.GITHUB_TOKEN }}
          ANTHROPIC_API_KEY: \${{ secrets.ANTHROPIC_API_KEY }}
        run: |
          dispatch run \\
            --max-issues \${{ github.event.inputs.max_issues || '${maxIssues}' }} \\${draft ? "\n            --draft \\" : ""}${labels.length > 0 ? `\n            --label ${labels.join(" ")} \\` : ""}
            \${{ github.event.inputs.dry_run == 'true' && '--dry-run' || '' }}

      - name: Upload run logs
        if: always()
        uses: actions/upload-artifact@v4
        with:
          name: dispatch-logs-\${{ github.run_id }}
          path: .dispatch/
          retention-days: 30
`;

