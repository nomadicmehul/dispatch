export function registerScheduleCommand(program: Command) {
  program
    .command("schedule")
    .description("Generate a GitHub Actions workflow for nightly scheduled runs")
    .option("--time <time>", "Time to run in UTC (e.g., '2am', '03:00', 'midnight')", "2am")
    .option("--max-issues <n>", "Max issues per nightly run", parseInt)
    .option("--draft", "Create PRs as drafts")
    .option("--label <labels...>", "Only process issues with these labels")
    .option("--cron <expression>", "Custom cron expression (overrides --time)")
    .option("--auth <method>", "Auth method: api-key (personal) or claude-code (enterprise, no API key)", "api-key")
    .option("--stdout", "Print workflow to stdout instead of writing file")
    .action(async (options) => {
      try {
        const cwd = process.cwd();

        // Verify we're in a git repo
        let owner: string, repo: string;
        try {
          ({ owner, repo } = await getRepoInfo(cwd));
        } catch {
          log.error("Not in a Git repository with a GitHub remote.");
          log.info("Run this command from the root of your GitHub repository.");
          process.exit(1);
          return;
        }

        log.header("Dispatch Schedule Setup");
        log.info(`Repository: ${chalk.bold(`${owner}/${repo}`)}`);

        const cronSchedule = options.cron || parseCronTime(options.time);
        const maxIssues = options.maxIssues || 10;
        const draft = options.draft || false;
        const labels = options.label || [];

        const auth = (options.auth || "api-key") as AuthMethod;
        if (!["api-key", "claude-code"].includes(auth)) {
          log.error(`Unknown auth method: "${auth}". Use "api-key" (personal) or "claude-code" (enterprise).`);
          process.exit(1);
          return;
        }

        const workflow = generateWorkflow(cronSchedule, maxIssues, draft, labels, auth);

        if (options.stdout) {
          console.log(workflow);
          return;
        }

        // Write the workflow file
        const workflowDir = join(cwd, ".github", "workflows");
        const workflowPath = join(workflowDir, "dispatch-nightly.yml");

        await mkdir(workflowDir, { recursive: true });

        // Check if file already exists
        let exists = false;
        try {
          await access(workflowPath);
          exists = true;
        } catch {
          // doesn't exist — good
        }

        await writeFile(workflowPath, workflow, "utf-8");

        console.log();

        if (exists) {
          log.warn(`Updated existing workflow: ${chalk.bold(workflowPath)}`);
        } else {
          log.success(`Created workflow: ${chalk.bold(workflowPath)}`);
        }

        console.log();
        console.log(chalk.bold("  Schedule:"));
        console.log(chalk.gray(`    Cron: ${cronSchedule}`));
        console.log(chalk.gray(`    Time: ${options.time || "2:00 AM"} UTC (daily)`));
        console.log(chalk.gray(`    Max issues: ${maxIssues}`));
        if (labels.length > 0) {
          console.log(chalk.gray(`    Labels: ${labels.join(", ")}`));
        }
        console.log();
        printAuthInstructions(auth, owner, repo);
        console.log();
        console.log(chalk.bold("  Alternative — Local cron job:"));
        console.log(chalk.gray(`    Add this to your crontab (${chalk.yellow("crontab -e")}):`));
        console.log(chalk.gray(`    ${chalk.yellow(`${cronSchedule} cd ${cwd} && dispatch run >> /tmp/dispatch.log 2>&1`)}`));
        console.log();
      } catch (err) {
        log.error(`Schedule setup failed: ${err instanceof Error ? err.message : err}`);
        process.exit(1);
      }
    });
}
